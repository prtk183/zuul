package com.szuul.wrappers;

import java.util.List;
import java.util.Map;

public class WrapperDenomination {

 /**-------DenominationTable--------.*/ 
 Map<Integer, Integer> DenominationTable;
  


/**-------Permisssion--------.*/
  Boolean permission;
  
  public WrapperDenomination(Map<Integer, Integer> denominationTable, Boolean permission) {
    super();
    DenominationTable = denominationTable;
    this.permission = permission;
  }


  @Override
  public String toString() {
    return "WrapperDenomination [DenominationTable=" + DenominationTable + ", permission="
        + permission + "]";
  }


  /**
   * @return
   */
  public Map<Integer, Integer> getDenominationTable() {
    return DenominationTable;
  }


  /**
   * @param denominationTable
   */
  public void setDenominationTable(Map<Integer, Integer> denominationTable) {
    DenominationTable = denominationTable;
  }


  /**
   * @return
   */
  public Boolean getPermission() {
    return permission;
  }

  
  /**
   * @param permission
   */
  public void setPermission(Boolean permission) {
    this.permission = permission;
  }

 
  
}
